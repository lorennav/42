#include <stdlib.h>

int		ft_strlen(char *str)
{
	int		i;

	i = 0;
	while (str[i])
		i++;
	return (i);
}

char	*ft_strcpy(char *dest, char *src)
{
	int		i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

char	*ft_strdup(char *src)
{
	char	*tab;
	int		size_src;

	size_src = ft_strlen(src);
	tab = malloc(sizeof(*tab) * size_src + 1);
	ft_strcpy(tab, src);
	return (tab);
}
