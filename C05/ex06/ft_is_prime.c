int		ft_is_prime(int nb)
{
	long long	i;
	long long	d;

	i = 2;
	d = 0;
	if (nb == 0 || nb == 1 || nb < 0)
		return (0);
	while (i <= nb / i)
	{
		if (nb % i == 0)
			return (0);
		i++;
	}
	return (1);
}
